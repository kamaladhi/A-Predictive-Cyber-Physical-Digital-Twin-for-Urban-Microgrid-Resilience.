@dataclass
class GenDispatchCommand:
    """Command for generator dispatch coordination."""
    microgrid_id: str
    generator_on: bool
    power_setpoint_kw: float
    fuel_share_for_mg: Optional[str] = None  # Destination MG if shared
    reason: str = ""


class SharedGenScheduler:
    """
    Cross-microgrid generator scheduling during outages.

    Priority rules:
    - Hospital generators NEVER shared (dedicated critical backup)
    - Other generators shared only if donor SOC > threshold
    - Dispatch to highest-priority deficit first
    - Respect minimum fuel reserve per MG priority
    """

    # Minimum fuel reserve (hours at rated power) by priority
    MIN_FUEL_RESERVE = {
        MicrogridPriority.CRITICAL: 8.0,
        MicrogridPriority.HIGH: 4.0,
        MicrogridPriority.MEDIUM: 2.0,
        MicrogridPriority.LOW: 1.0,
    }

    # Minimum SOC for generator sharing eligibility
    MIN_SHARE_SOC = {
        MicrogridPriority.CRITICAL: 999.0,  # Hospital: never share
        MicrogridPriority.HIGH: 60.0,
        MicrogridPriority.MEDIUM: 40.0,
        MicrogridPriority.LOW: 50.0,
    }

    def __init__(self):
        self.dispatch_history: List[GenDispatchCommand] = []
        self.total_shared_kwh = 0.0
        self.per_step_records: List[dict] = []  # Per-step donor/recipient tracking

    def schedule(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        mg_infos: Dict,
        dt_hours: float = 0.25,
    ) -> Dict[str, GenDispatchCommand]:
        """
        Compute generator dispatch commands across all microgrids.

        Algorithm:
        1. Identify MGs with surplus generator capacity
        2. Identify MGs with deficit (load > generation + battery)
        3. Sort donors by efficiency, recipients by priority
        4. Allocate surplus gen to deficits, respecting fuel reserves

        Returns:
            Dict of GenDispatchCommand per microgrid.
        """
        commands = {}

        # Identify surplus (donors) and deficit (recipients)
        donors = []
        recipients = []

        for mg_id, status in mg_statuses.items():
            mg_info = mg_infos.get(mg_id)
            if not mg_info:
                continue

            net_balance = (
                status.pv_generation_kw
                + status.generator_power_kw
                + max(0, status.battery_power_kw)  # Discharge only
                - status.total_load_kw
            )

            # Check if this MG can share gen
            share_soc = self.MIN_SHARE_SOC.get(mg_info.priority, 999.0)

            if (net_balance > 10.0
                    and status.battery_soc_percent > share_soc
                    and mg_info.priority != MicrogridPriority.CRITICAL):
                # Has surplus and eligible to share
                # Check fuel reserve
                fuel_reserve_hours = self._fuel_hours_remaining(
                    status, mg_info
                )
                min_reserve = self.MIN_FUEL_RESERVE.get(
                    mg_info.priority, 2.0
                )

                if fuel_reserve_hours > min_reserve:
                    donors.append({
                        'mg_id': mg_id,
                        'available_kw': net_balance * 0.5,  # Share half
                        'fuel_hours': fuel_reserve_hours,
                        'priority': mg_info.priority,
                    })

            elif net_balance < -5.0 or status.load_shed_kw > 5.0:
                # Has deficit or is shedding
                deficit = max(abs(net_balance), status.load_shed_kw)
                recipients.append({
                    'mg_id': mg_id,
                    'deficit_kw': deficit,
                    'priority': mg_info.priority,
                    'critical_at_risk': status.critical_load_shed,
                })

        # Sort: donors by available power (largest first)
        donors.sort(key=lambda d: -d['available_kw'])

        # Sort: recipients by priority (CRITICAL first), then deficit
        recipients.sort(key=lambda r: (
            0 if r['critical_at_risk'] else 1,
            r['priority'].value,
            -r['deficit_kw'],
        ))

        # Allocate
        remaining_donor = {d['mg_id']: d['available_kw'] for d in donors}

        for recipient in recipients:
            rec_id = recipient['mg_id']
            remaining_need = recipient['deficit_kw']

            for donor in donors:
                don_id = donor['mg_id']
                available = remaining_donor.get(don_id, 0)

                if available < 5.0 or remaining_need < 5.0:
                    continue

                allocated = min(available, remaining_need)
                remaining_donor[don_id] -= allocated
                remaining_need -= allocated

                cmd = GenDispatchCommand(
                    microgrid_id=don_id,
                    generator_on=True,
                    power_setpoint_kw=allocated,
                    fuel_share_for_mg=rec_id,
                    reason=f"Shared gen: {don_id}→{rec_id} ({allocated:.0f}kW)"
                )
                commands[f"{don_id}_for_{rec_id}"] = cmd
                self.total_shared_kwh += allocated * dt_hours
                self.dispatch_history.append(cmd)

                self.per_step_records.append({
                    'donor': don_id,
                    'recipient': rec_id,
                    'kw': allocated,
                    'donor_fuel_remaining': (
                        mg_statuses[don_id].fuel_remaining_liters
                        if don_id in mg_statuses else 0
                    ),
                    'donor_soc': (
                        mg_statuses[don_id].battery_soc_percent
                        if don_id in mg_statuses else 0
                    ),
                })

                logger.info(cmd.reason)

                if remaining_need < 5.0:
                    break

        return commands

    @staticmethod
    def _fuel_hours_remaining(status: MicrogridStatus, mg_info) -> float:
        """Estimate fuel-hours remaining at current generator output."""
        if status.generator_power_kw <= 0:
            return 999.0
        # Rough: 0.25 L/kWh fuel consumption
        fuel_kwh = status.fuel_remaining_liters / 0.25
        return fuel_kwh / max(1.0, status.generator_power_kw)


