@dataclass
class StandardReliabilityIndices:
    """Maps simulation metrics to IEEE Std 1366-2012 reliability indices.

    Reference: IEEE Std 1366-2012, "IEEE Guide for Electric Power
    Distribution Reliability Indices."
    """
    SAIDI: float = 0.0   # System Avg Interruption Duration Index (hours)
    SAIFI: float = 0.0   # System Avg Interruption Frequency Index
    CAIDI: float = 0.0   # Customer Avg Interruption Duration (hours)
    EENS: float = 0.0    # Expected Energy Not Served (kWh)
    LOLP: float = 0.0    # Loss of Load Probability (fraction)
    ASAI: float = 0.0    # Average Service Availability Index


@dataclass
class CoordinatedOutageScorecard:
    """Extended scorecard with coordination-specific metrics."""

    # Core resilience (from existing)
    city_survivability_index: float = 1.0
    total_unserved_energy_kwh: float = 0.0
    critical_unserved_energy_kwh: float = 0.0
    recovery_time_hours: float = 0.0

    # Coordination-specific
    cascade_events_triggered: int = 0
    cascade_events_prevented: int = 0
    shared_generation_kwh: float = 0.0
    demand_reduction_kwh: float = 0.0
    energy_exchanged_kwh: float = 0.0
    recovery_phases_completed: int = 0
    secondary_instability_events: int = 0
    mean_recovery_time_hours: float = 0.0
    generator_usage_total_kwh: float = 0.0
    generator_usage_reduction_percent: float = 0.0

    # IEEE 1366-2012 standard indices
    standard_indices: Optional[StandardReliabilityIndices] = None

    # Per-outage breakdown
    outage_metrics: List[dict] = field(default_factory=list)


class OutageMetricsCollector:
    """
    Accumulates per-timestep metrics during simulation.
    Produces a CoordinatedOutageScorecard at simulation end.

    Granular tracking includes per-MG ENS, SOC, gen power, and
    shed percentage at each timestep for DataFrame export.
    """

    N_MICROGRIDS = 4  # Used for SAIDI/SAIFI computation

    def __init__(self):
        self.total_ens_kwh = 0.0
        self.critical_ens_kwh = 0.0
        self.gen_energy_kwh = 0.0
        self.shared_energy_kwh = 0.0
        self.exchanged_energy_kwh = 0.0
        self.demand_reduction_kwh = 0.0
        self.outage_start: Optional[datetime] = None
        self.outage_end: Optional[datetime] = None
        self.recovery_start: Optional[datetime] = None
        self.recovery_end: Optional[datetime] = None
        self.timestep_data: List[dict] = []
        # Per-MG ENS tracking
        self.per_mg_ens: Dict[str, float] = {}
        # Interruption tracking for standard indices
        self._interruption_events: int = 0
        self._interruption_mg_hours: float = 0.0  # Sum(interrupted_MGs × dt)
        self._steps_with_shedding: int = 0
        self._total_steps: int = 0

    def update(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        resource_transfers: List,
        recovery_phase: RecoveryPhase,
        gen_shared_kwh: float = 0.0,
        demand_reduced_kwh: float = 0.0,
        dt_hours: float = 0.25,
    ):
        """Per-timestep metric update with granular per-MG tracking."""
        self._total_steps += 1
        per_mg_shed = {}
        per_mg_gen = {}
        per_mg_soc = {}
        interrupted_count = 0

        for mg_id, status in mg_statuses.items():
            shed_kwh = status.load_shed_kw * dt_hours
            self.total_ens_kwh += shed_kwh
            self.per_mg_ens[mg_id] = self.per_mg_ens.get(mg_id, 0) + shed_kwh
            if status.critical_load_shed:
                self.critical_ens_kwh += shed_kwh

            self.gen_energy_kwh += status.generator_power_kw * dt_hours

            per_mg_shed[mg_id] = status.load_shed_kw
            per_mg_gen[mg_id] = status.generator_power_kw
            per_mg_soc[mg_id] = status.battery_soc_percent

            if status.is_islanded or status.load_shed_kw > 0:
                interrupted_count += 1

        # Track interruptions for SAIDI/SAIFI
        if interrupted_count > 0:
            self._steps_with_shedding += 1
            self._interruption_mg_hours += interrupted_count * dt_hours
            self._interruption_events += 1

        # Resource sharing transfers
        if resource_transfers:
            for t in resource_transfers:
                self.exchanged_energy_kwh += t.delivered_kw * dt_hours

        self.shared_energy_kwh += gen_shared_kwh
        self.demand_reduction_kwh += demand_reduced_kwh

        # Track outage timing
        any_islanded = any(s.is_islanded for s in mg_statuses.values())
        ts = next(iter(mg_statuses.values())).timestamp if mg_statuses else None

        if any_islanded and self.outage_start is None:
            self.outage_start = ts
        if not any_islanded and self.outage_start and self.outage_end is None:
            self.outage_end = ts

        if recovery_phase != RecoveryPhase.INACTIVE:
            if self.recovery_start is None:
                self.recovery_start = ts
            if recovery_phase == RecoveryPhase.COMPLETE:
                self.recovery_end = ts

        # City deficit
        total_load = sum(s.total_load_kw for s in mg_statuses.values())
        total_gen = sum(
            s.pv_generation_kw + s.generator_power_kw
            for s in mg_statuses.values()
        )

        # Store granular timestep snapshot
        self.timestep_data.append({
            'timestamp': ts,
            'total_ens_kwh': self.total_ens_kwh,
            'gen_energy_kwh': self.gen_energy_kwh,
            'exchanged_kwh': self.exchanged_energy_kwh,
            'recovery_phase': recovery_phase.name,
            'per_mg_shed': dict(per_mg_shed),
            'per_mg_gen_kw': dict(per_mg_gen),
            'per_mg_soc': dict(per_mg_soc),
            'city_deficit_kw': max(0, total_load - total_gen),
        })

    def compute_standard_indices(self) -> StandardReliabilityIndices:
        """
        Compute IEEE Std 1366-2012 reliability indices from simulation data.

        Formulae:
        - SAIDI = Σ(r_i × N_i) / N_T  [hours]
        - SAIFI = Σ(N_i) / N_T
        - CAIDI = SAIDI / SAIFI  [hours]
        - EENS  = total_ens_kwh  [kWh]
        - LOLP  = (steps with shedding) / total_steps
        - ASAI  = 1 − (interruption_hours / service_hours)
        """
        n_t = self.N_MICROGRIDS
        saidi = self._interruption_mg_hours / n_t if n_t > 0 else 0
        saifi = self._interruption_events / n_t if n_t > 0 else 0
        caidi = saidi / saifi if saifi > 0 else 0
        lolp = (
            self._steps_with_shedding / self._total_steps
            if self._total_steps > 0 else 0
        )
        total_service_hours = self._total_steps * 0.25 * n_t
        asai = (
            1 - self._interruption_mg_hours / total_service_hours
            if total_service_hours > 0 else 1.0
        )

        return StandardReliabilityIndices(
            SAIDI=round(saidi, 4),
            SAIFI=round(saifi, 4),
            CAIDI=round(caidi, 4),
            EENS=round(self.total_ens_kwh, 2),
            LOLP=round(lolp, 6),
            ASAI=round(asai, 6),
        )

    def to_dataframe(self):
        """
        Export per-timestep metrics as a pandas DataFrame.

        Columns: timestamp, total_ens_kwh, gen_energy_kwh, exchanged_kwh,
                 recovery_phase, city_deficit_kw, plus per-MG shed/gen/soc.

        Returns:
            pd.DataFrame if pandas is available, else list of dicts.
        """
        try:
            import pandas as pd
            rows = []
            for d in self.timestep_data:
                row = {
                    'timestamp': d['timestamp'],
                    'total_ens_kwh': d['total_ens_kwh'],
                    'gen_energy_kwh': d['gen_energy_kwh'],
                    'exchanged_kwh': d['exchanged_kwh'],
                    'recovery_phase': d['recovery_phase'],
                    'city_deficit_kw': d.get('city_deficit_kw', 0),
                }
                for mg_id, val in d.get('per_mg_shed', {}).items():
                    row[f'{mg_id}_shed_kw'] = val
                for mg_id, val in d.get('per_mg_gen_kw', {}).items():
                    row[f'{mg_id}_gen_kw'] = val
                for mg_id, val in d.get('per_mg_soc', {}).items():
                    row[f'{mg_id}_soc'] = val
                rows.append(row)
            return pd.DataFrame(rows)
        except ImportError:
            logger.warning("pandas not available; returning raw list of dicts")
            return self.timestep_data

    def finalize(
        self,
        cascade_controller: Optional['CascadeController'] = None,
        stability_monitor: Optional['StabilityMonitor'] = None,
        recovery_controller: Optional['RecoveryController'] = None,
        exchange_bus=None,
        baseline_gen_kwh: float = 0.0,
    ) -> CoordinatedOutageScorecard:
        """Compute final scorecard from accumulated data."""
        recovery_hours = 0.0
        if self.recovery_start and self.recovery_end:
            recovery_hours = (
                self.recovery_end - self.recovery_start
            ).total_seconds() / 3600.0

        gen_reduction = 0.0
        if baseline_gen_kwh > 0:
            gen_reduction = (
                (baseline_gen_kwh - self.gen_energy_kwh)
                / baseline_gen_kwh * 100.0
            )

        std_indices = self.compute_standard_indices()

        return CoordinatedOutageScorecard(
            total_unserved_energy_kwh=round(self.total_ens_kwh, 2),
            critical_unserved_energy_kwh=round(self.critical_ens_kwh, 2),
            recovery_time_hours=round(recovery_hours, 2),
            cascade_events_triggered=(
                cascade_controller.cascade_count if cascade_controller else 0
            ),
            cascade_events_prevented=(
                cascade_controller.cascades_prevented
                if cascade_controller else 0
            ),
            shared_generation_kwh=round(self.shared_energy_kwh, 2),
            demand_reduction_kwh=round(self.demand_reduction_kwh, 2),
            energy_exchanged_kwh=round(self.exchanged_energy_kwh, 2),
            recovery_phases_completed=(
                recovery_controller.phase.value
                if recovery_controller else 0
            ),
            secondary_instability_events=(
                stability_monitor.instability_events
                if stability_monitor else 0
            ),
            mean_recovery_time_hours=round(recovery_hours, 2),
            generator_usage_total_kwh=round(self.gen_energy_kwh, 2),
            generator_usage_reduction_percent=round(gen_reduction, 2),
            standard_indices=std_indices,
        )

