class StabilityMonitor:
    """
    Monitors for secondary instability during recovery.

    Checks:
    1. Load pickup rate: >20% capacity increase in one step → pause
    2. Battery SOC drop: >5% drop during recovery → slow down
    3. Generator fuel: <2h reserve → hold generators
    4. Aggregate load: >90% city capacity → staged approach
    """

    LOAD_PICKUP_LIMIT = 0.20
    SOC_DROP_LIMIT = 5.0
    FUEL_CRITICAL_HOURS = 2.0
    CITY_LOAD_LIMIT = 0.90

    def __init__(self):
        self.previous_loads: Dict[str, float] = {}
        self.previous_socs: Dict[str, float] = {}
        self.instability_events = 0
        self.events: List[StabilityEvent] = []

    def check_stability(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        mg_infos: Dict,
    ) -> Tuple[bool, str]:
        """Check system stability. Returns (is_stable, reason)."""
        ts = None
        if mg_statuses:
            first = next(iter(mg_statuses.values()))
            ts = getattr(first, 'timestamp', None)

        for mg_id, status in mg_statuses.items():
            mg_info = mg_infos.get(mg_id)
            if not mg_info:
                continue

            # Check 1: Load pickup rate
            prev_load = self.previous_loads.get(mg_id, status.total_load_kw)
            load_increase = (
                (status.total_load_kw - prev_load) / mg_info.total_capacity_kw
            )
            self.events.append(StabilityEvent(
                timestamp=ts, mg_id=mg_id, check_type='load_pickup',
                value=load_increase, threshold=self.LOAD_PICKUP_LIMIT,
                passed=load_increase <= self.LOAD_PICKUP_LIMIT,
            ))
            if load_increase > self.LOAD_PICKUP_LIMIT:
                self.instability_events += 1
                self._update_previous(mg_statuses)
                return False, (
                    f"{mg_id}: load pickup {load_increase:.0%} "
                    f"> {self.LOAD_PICKUP_LIMIT:.0%} limit"
                )

            # Check 2: Battery SOC drop
            prev_soc = self.previous_socs.get(
                mg_id, status.battery_soc_percent
            )
            soc_drop = prev_soc - status.battery_soc_percent
            self.events.append(StabilityEvent(
                timestamp=ts, mg_id=mg_id, check_type='soc_drop',
                value=soc_drop, threshold=self.SOC_DROP_LIMIT,
                passed=soc_drop <= self.SOC_DROP_LIMIT,
            ))
            if soc_drop > self.SOC_DROP_LIMIT:
                self.instability_events += 1
                self._update_previous(mg_statuses)
                return False, (
                    f"{mg_id}: SOC drop {soc_drop:.1f}% > {self.SOC_DROP_LIMIT}%"
                )

            # Check 3: Fuel critical
            if status.fuel_remaining_liters > 0:
                fuel_hours = status.fuel_remaining_liters / max(
                    1.0, status.generator_power_kw * 0.25
                )
                self.events.append(StabilityEvent(
                    timestamp=ts, mg_id=mg_id, check_type='fuel_critical',
                    value=fuel_hours, threshold=self.FUEL_CRITICAL_HOURS,
                    passed=fuel_hours >= self.FUEL_CRITICAL_HOURS,
                ))
                if fuel_hours < self.FUEL_CRITICAL_HOURS:
                    self.instability_events += 1
                    self._update_previous(mg_statuses)
                    return False, f"{mg_id}: fuel < {self.FUEL_CRITICAL_HOURS}h"

        # Check 4: City-wide utilization
        total_load = sum(s.total_load_kw for s in mg_statuses.values())
        total_capacity = sum(
            mg_infos[mg_id].total_capacity_kw
            for mg_id in mg_statuses
            if mg_id in mg_infos
        )
        if total_capacity > 0:
            city_util = total_load / total_capacity
            self.events.append(StabilityEvent(
                timestamp=ts, mg_id='city',
                check_type='city_load',
                value=city_util, threshold=self.CITY_LOAD_LIMIT,
                passed=city_util <= self.CITY_LOAD_LIMIT,
            ))
            if city_util > self.CITY_LOAD_LIMIT:
                self.instability_events += 1
                self._update_previous(mg_statuses)
                return False, (
                    f"City load {city_util:.0%} > {self.CITY_LOAD_LIMIT:.0%}"
                )

        self._update_previous(mg_statuses)
        return True, "Stable"

    def get_event_distribution(self) -> Dict[str, int]:
        """Return count of events by check_type (failed checks only)."""
        dist: Dict[str, int] = {}
        for e in self.events:
            if not e.passed:
                dist[e.check_type] = dist.get(e.check_type, 0) + 1
        return dist

    def _update_previous(self, statuses: Dict[str, MicrogridStatus]):
        for mg_id, status in statuses.items():
            self.previous_loads[mg_id] = status.total_load_kw
            self.previous_socs[mg_id] = status.battery_soc_percent


