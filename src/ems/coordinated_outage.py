class OutageSeverity(Enum):
    """Severity levels for regional outage events."""
    MINOR = 1         # Single feeder, 1-2 MGs, <2h
    MODERATE = 2      # Distribution fault, 2-3 MGs, 2-6h
    MAJOR = 3         # Substation failure, all MGs, 6-24h
    CATASTROPHIC = 4  # Transmission failure, all MGs, 24-72h


@dataclass
class RegionalOutageEvent:
    """Extended outage event with severity, probability, and cascade support."""
    event_id: str
    start_time: datetime
    duration_hours: float
    affected_microgrids: List[str]
    severity: OutageSeverity
    description: str = ""

    # Probabilistic fields
    probability: float = 1.0            # Occurrence probability (for Monte Carlo)
    cascade_probability: float = 0.0    # Probability of triggering cascade
    propagation_delay_hours: float = 0.0  # Delay before cascading to neighbors
    weather_correlation: float = 0.0    # 0-1, correlation with weather events

    # Tracking
    is_cascade_event: bool = False      # True if generated by cascade logic
    parent_event_id: Optional[str] = None  # Source event that triggered cascade
    cascade_depth: int = 0              # Current cascade depth

    @property
    def end_time(self) -> datetime:
        return self.start_time + timedelta(hours=self.duration_hours)


