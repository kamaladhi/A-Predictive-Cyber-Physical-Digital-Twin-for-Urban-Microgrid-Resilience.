@dataclass
class CascadeAnalysisRecord:
    """Per-decision record for cascade analysis and sensitivity studies."""
    timestamp: datetime
    mg_id: str
    utilization: float
    threshold: float
    cascade_triggered: bool
    cascade_depth: int
    parent_event_id: str
    additional_ens_kwh: float = 0.0  # ENS caused by this cascade


class CascadeController:
    """
    Simplified cascading failure model (no power flow physics).

    Cascade trigger: When a MG is islanded and its neighbors absorb extra
    load via the exchange bus, check if any neighbor exceeds capacity.
    If so, inject a delayed secondary fault event.

    Parameter justification:
    - OVERLOAD_THRESHOLD=1.10: IEEE Std 1547-2018 §8.2.1 specifies abnormal
      operating conditions trip at >110% rated voltage/current.
    - MAX_CASCADE_DEPTH=2: Standard N−1/N−2 contingency analysis; with a
      4-node network (diameter=2), deeper cascades are topologically impossible.
    - CASCADE_DELAY=0.25h: Matches 15-min simulation timestep (protection
      relay coordination timescale).
    """

    def __init__(
        self,
        overload_threshold: float = 1.10,
        max_cascade_depth: int = 2,
        cascade_duration_hours: float = 1.0,
        cascade_delay_hours: float = 0.25,
    ):
        self.overload_threshold = overload_threshold
        self.max_cascade_depth = max_cascade_depth
        self.cascade_duration_hours = cascade_duration_hours
        self.cascade_delay_hours = cascade_delay_hours

        self.cascade_events: List[RegionalOutageEvent] = []
        self.cascade_count = 0
        self.cascades_prevented = 0
        self.analysis_records: List[CascadeAnalysisRecord] = []

    def check_cascade(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        mg_infos: Dict,
        currently_faulted: set,
        current_time: datetime,
        parent_event_id: str = "manual",
        current_depth: int = 0,
    ) -> List[RegionalOutageEvent]:
        """
        Check if any non-faulted MG is overloaded → trigger secondary fault.
        """
        if current_depth >= self.max_cascade_depth:
            return []

        new_faults = []

        for mg_id, status in mg_statuses.items():
            if mg_id in currently_faulted:
                continue

            mg_info = mg_infos.get(mg_id)
            if not mg_info:
                continue

            if mg_info.total_capacity_kw <= 0:
                continue

            utilization = status.total_load_kw / mg_info.total_capacity_kw

            if utilization > self.overload_threshold:
                cascade_event = RegionalOutageEvent(
                    event_id=f"cascade_{uuid.uuid4().hex[:8]}",
                    start_time=current_time + timedelta(
                        hours=self.cascade_delay_hours
                    ),
                    duration_hours=self.cascade_duration_hours,
                    affected_microgrids=[mg_id],
                    severity=OutageSeverity.MINOR,
                    description=(
                        f"Cascade from {parent_event_id}: "
                        f"{mg_id} overloaded at {utilization:.0%}"
                    ),
                    is_cascade_event=True,
                    parent_event_id=parent_event_id,
                    cascade_depth=current_depth + 1,
                )
                new_faults.append(cascade_event)
                self.cascade_count += 1

                self.analysis_records.append(CascadeAnalysisRecord(
                    timestamp=current_time, mg_id=mg_id,
                    utilization=utilization,
                    threshold=self.overload_threshold,
                    cascade_triggered=True,
                    cascade_depth=current_depth + 1,
                    parent_event_id=parent_event_id,
                ))

                logger.warning(
                    f"CASCADE: {mg_id} overloaded at {utilization:.0%} → "
                    f"secondary fault in {self.cascade_delay_hours}h "
                    f"(depth={current_depth + 1})"
                )
            else:
                if utilization > 0.95:
                    self.cascades_prevented += 1
                    self.analysis_records.append(CascadeAnalysisRecord(
                        timestamp=current_time, mg_id=mg_id,
                        utilization=utilization,
                        threshold=self.overload_threshold,
                        cascade_triggered=False,
                        cascade_depth=current_depth,
                        parent_event_id=parent_event_id,
                    ))
                    logger.info(
                        f"CASCADE AVOIDED: {mg_id} at {utilization:.0%} "
                        f"(threshold {self.overload_threshold:.0%})"
                    )

        self.cascade_events.extend(new_faults)
        return new_faults

    def get_analysis_records(self) -> List[CascadeAnalysisRecord]:
        """Return all cascade analysis records for post-hoc study."""
        return list(self.analysis_records)


class CascadeSensitivityAnalyzer:
    """
    Sweep cascade parameters to evaluate robustness.

    Sweeps OVERLOAD_THRESHOLD from 1.00 to 1.30 (step 0.02) and
    MAX_CASCADE_DEPTH from 1 to 4. For each pair, collects:
    - cascade_count, total_cascade_duration_hours
    - additional_ens_kwh, cascade_prevention_rate
    """

    DEFAULT_THRESHOLDS = [round(1.00 + i * 0.02, 2) for i in range(16)]
    DEFAULT_DEPTHS = [1, 2, 3, 4]

    @staticmethod
    def sweep(
        thresholds: Optional[List[float]] = None,
        depths: Optional[List[int]] = None,
    ) -> List[dict]:
        """
        Generate parameter configurations for sensitivity analysis.

        Usage:
            configs = CascadeSensitivityAnalyzer.sweep()
            for cfg in configs:
                controller = CascadeController(**cfg)
                # ... run simulation ...
                results.append({**cfg, 'cascade_count': controller.cascade_count})

        Returns:
            List of dicts with constructor kwargs for CascadeController.
        """
        thresholds = thresholds or CascadeSensitivityAnalyzer.DEFAULT_THRESHOLDS
        depths = depths or CascadeSensitivityAnalyzer.DEFAULT_DEPTHS

        configs = []
        for thresh in thresholds:
            for depth in depths:
                configs.append({
                    'overload_threshold': thresh,
                    'max_cascade_depth': depth,
                })
        return configs

    @staticmethod
    def summarize_results(results: List[dict]) -> dict:
        """
        Aggregate sweep results for table/heatmap generation.

        Args:
            results: List of dicts with keys:
                overload_threshold, max_cascade_depth,
                cascade_count, cascades_prevented,
                total_ens_kwh

        Returns:
            Dict keyed by (threshold, depth) → aggregated metrics.
        """
        summary = {}
        for r in results:
            key = (r['overload_threshold'], r['max_cascade_depth'])
            if key not in summary:
                summary[key] = {
                    'runs': 0, 'total_cascades': 0,
                    'total_prevented': 0, 'total_ens': 0.0,
                }
            summary[key]['runs'] += 1
            summary[key]['total_cascades'] += r.get('cascade_count', 0)
            summary[key]['total_prevented'] += r.get('cascades_prevented', 0)
            summary[key]['total_ens'] += r.get('total_ens_kwh', 0.0)

        # Compute averages
        for key, v in summary.items():
            n = max(1, v['runs'])
            v['mean_cascades'] = v['total_cascades'] / n
            v['mean_prevented'] = v['total_prevented'] / n
            v['mean_ens'] = v['total_ens'] / n
            if v['total_cascades'] + v['total_prevented'] > 0:
                v['prevention_rate'] = (
                    v['total_prevented']
                    / (v['total_cascades'] + v['total_prevented'])
                )
            else:
                v['prevention_rate'] = 1.0

        return summary


