# PROBABILISTIC OUTAGE GENERATOR
# =============================================================================

class RegionalOutageGenerator:
    """
    Monte Carlo outage generation using Poisson arrival + Weibull duration.

    Parameters are based on IEEE RBTS (Roy Billinton Test System)
    benchmarks adapted for tropical urban distribution networks.
    """

    # Outage rate parameters: lambda (events/day), shape k, scale (hours)
    RATE_PARAMS = {
        OutageSeverity.MINOR:        {'lambda': 0.15, 'k': 1.5, 'scale': 1.5},
        OutageSeverity.MODERATE:     {'lambda': 0.05, 'k': 2.0, 'scale': 4.0},
        OutageSeverity.MAJOR:        {'lambda': 0.01, 'k': 1.8, 'scale': 12.0},
        OutageSeverity.CATASTROPHIC: {'lambda': 0.002, 'k': 2.5, 'scale': 36.0},
    }

    # Geographic proximity: probability that MG_j is co-affected when MG_i fails
    PROXIMITY_MATRIX = {
        ('hospital', 'university'):   0.7,   # Same district
        ('hospital', 'industrial'):   0.3,   # Adjacent districts
        ('hospital', 'residential'):  0.4,
        ('university', 'industrial'): 0.5,
        ('university', 'residential'): 0.6,  # Same district
        ('industrial', 'residential'): 0.3,
    }

    # Cascade probability by severity
    CASCADE_PROB = {
        OutageSeverity.MINOR: 0.05,
        OutageSeverity.MODERATE: 0.15,
        OutageSeverity.MAJOR: 0.30,
        OutageSeverity.CATASTROPHIC: 0.50,
    }

    ALL_MICROGRIDS = ['hospital', 'university', 'industrial', 'residential']

    def __init__(self, seed: Optional[int] = None):
        self.rng = np.random.default_rng(seed)

    def generate_scenario(
        self,
        duration_days: float,
        start_time: datetime,
        severity_filter: Optional[List[OutageSeverity]] = None,
    ) -> List[RegionalOutageEvent]:
        """
        Generate stochastic outage timeline for simulation period.

        Args:
            duration_days: Simulation period in days.
            start_time: Simulation start time.
            severity_filter: Only generate events of these severities (None = all).

        Returns:
            List of RegionalOutageEvent sorted by start_time.
        """
        events = []
        severities = severity_filter or list(OutageSeverity)

        for severity in severities:
            params = self.RATE_PARAMS[severity]
            lam = params['lambda']

            # Generate event count via Poisson
            n_events = self.rng.poisson(lam * duration_days)

            for _ in range(n_events):
                # Random arrival time (uniform within period)
                offset_hours = self.rng.uniform(0, duration_days * 24)
                event_start = start_time + timedelta(hours=offset_hours)

                # Duration via Weibull
                duration = float(self.rng.weibull(params['k']) * params['scale'])
                duration = max(0.25, min(duration, 72.0))  # Clamp: 15min to 72h

                # Primary affected microgrid (random)
                primary_mg = self.rng.choice(self.ALL_MICROGRIDS)
                affected = [primary_mg]

                # Co-affected microgrids based on proximity
                if severity != OutageSeverity.MINOR:
                    for other_mg in self.ALL_MICROGRIDS:
                        if other_mg == primary_mg:
                            continue
                        key = tuple(sorted([primary_mg, other_mg]))
                        proximity = self.PROXIMITY_MATRIX.get(key, 0.1)

                        # Higher severity → higher co-failure probability
                        severity_boost = (severity.value - 1) * 0.15
                        if self.rng.random() < proximity + severity_boost:
                            affected.append(other_mg)

                # For MAJOR/CATASTROPHIC: always affect all MGs
                if severity in [OutageSeverity.MAJOR, OutageSeverity.CATASTROPHIC]:
                    affected = list(self.ALL_MICROGRIDS)

                event = RegionalOutageEvent(
                    event_id=f"outage_{uuid.uuid4().hex[:8]}",
                    start_time=event_start,
                    duration_hours=round(duration, 2),
                    affected_microgrids=affected,
                    severity=severity,
                    description=f"{severity.name} outage affecting {len(affected)} MGs",
                    cascade_probability=self.CASCADE_PROB[severity],
                )
                events.append(event)

        # Sort by start time
        events.sort(key=lambda e: e.start_time)
        logger.info(
            f"Generated {len(events)} outage events over {duration_days} days "
            f"(severities: {[s.name for s in severities]})"
        )
        return events


