# DEMAND REDUCTION PROPAGATION
# =============================================================================

class DemandReductionPropagator:
    """
    Propagates load shedding decisions across microgrids based on
    city-wide power balance and priority weights.
    """

    SHED_LIMITS = {
        MicrogridPriority.CRITICAL: 0.0,   # Hospital: never shed
        MicrogridPriority.HIGH: 30.0,      # University: up to 30%
        MicrogridPriority.MEDIUM: 70.0,    # Industrial: up to 70%
        MicrogridPriority.LOW: 90.0,       # Residential: up to 90%
    }

    # Restoration order (highest priority restored first)
    RESTORATION_ORDER = [
        MicrogridPriority.CRITICAL,
        MicrogridPriority.HIGH,
        MicrogridPriority.MEDIUM,
        MicrogridPriority.LOW,
    ]

    SHED_STEP_PERCENT = 10.0  # Staged reduction: 10% increments

    def __init__(self):
        self.current_shed: Dict[str, float] = {}  # mg_id → shed_percent
        self.total_reduction_kwh = 0.0
        self.shed_history: List[Dict[str, float]] = []  # Per-step shed snapshots

    def propagate(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        mg_infos: Dict,
        city_deficit_kw: float,
        dt_hours: float = 0.25,
    ) -> Dict[str, float]:
        """
        Compute priority-weighted load shedding allocation.

        Args:
            mg_statuses: Current MG statuses.
            mg_infos: MG info registry.
            city_deficit_kw: Total city-wide power deficit (positive = deficit).
            dt_hours: Timestep duration.

        Returns:
            Dict of mg_id → target shed percent.
        """
        if city_deficit_kw <= 0:
            # No deficit → gradually restore
            return self._restore_loads(mg_infos)

        # Calculate shedding allocation: lowest priority sheds first
        allocation = {}
        remaining_deficit = city_deficit_kw

        # Process in reverse priority (LOW → CRITICAL)
        priority_groups = self._group_by_priority(mg_statuses, mg_infos)

        for priority in reversed(self.RESTORATION_ORDER):
            if remaining_deficit <= 0:
                break

            group = priority_groups.get(priority, [])
            max_shed = self.SHED_LIMITS.get(priority, 0.0)

            for mg_id, status in group:
                if remaining_deficit <= 0:
                    break

                current = self.current_shed.get(mg_id, 0.0)
                # Increase shed by one step, capped at limit
                new_shed = min(current + self.SHED_STEP_PERCENT, max_shed)

                shed_power = status.total_load_kw * (new_shed / 100.0)
                current_power = status.total_load_kw * (current / 100.0)
                gained = shed_power - current_power

                allocation[mg_id] = new_shed
                self.current_shed[mg_id] = new_shed
                remaining_deficit -= gained

                if gained > 0:
                    self.total_reduction_kwh += gained * dt_hours

        # MGs not needing shed keep their current level
        for mg_id in mg_statuses:
            if mg_id not in allocation:
                allocation[mg_id] = self.current_shed.get(mg_id, 0.0)

        self.shed_history.append(dict(allocation))
        return allocation

    def get_priority_violation_count(self, mg_infos: Dict) -> int:
        """Count steps where higher-priority MG sheds while lower-priority has capacity."""
        violations = 0
        for snapshot in self.shed_history:
            priority_shedding = {}
            for mg_id, shed_pct in snapshot.items():
                info = mg_infos.get(mg_id)
                if info:
                    priority_shedding[info.priority] = max(
                        priority_shedding.get(info.priority, 0.0), shed_pct
                    )
            # Check: higher priority shedding while lower priority has room
            for p_high in [MicrogridPriority.CRITICAL, MicrogridPriority.HIGH]:
                for p_low in [MicrogridPriority.MEDIUM, MicrogridPriority.LOW]:
                    if (priority_shedding.get(p_high, 0) > 0
                            and priority_shedding.get(p_low, 0) < 50):
                        violations += 1
                        break
        return violations

    def _restore_loads(self, mg_infos: Dict) -> Dict[str, float]:
        """Gradually restore loads in priority order."""
        allocation = {}
        for priority in self.RESTORATION_ORDER:
            for mg_id, info in mg_infos.items():
                if info.priority == priority:
                    current = self.current_shed.get(mg_id, 0.0)
                    new_shed = max(0.0, current - self.SHED_STEP_PERCENT)
                    allocation[mg_id] = new_shed
                    self.current_shed[mg_id] = new_shed
        return allocation

    @staticmethod
    def _group_by_priority(
        statuses: Dict[str, MicrogridStatus],
        infos: Dict,
    ) -> Dict[MicrogridPriority, List[Tuple[str, MicrogridStatus]]]:
        """Group microgrids by priority level."""
        groups: Dict[MicrogridPriority, List] = {}
        for mg_id, status in statuses.items():
            info = infos.get(mg_id)
            if not info:
                continue
            groups.setdefault(info.priority, []).append((mg_id, status))
        return groups


