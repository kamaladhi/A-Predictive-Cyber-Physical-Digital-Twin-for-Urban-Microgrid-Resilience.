class RecoveryPhase(Enum):
    """Staged recovery phases."""
    INACTIVE = 0
    PREPARATION = 1
    CRITICAL_RECONNECT = 2
    STAGED_RESTORATION = 3
    STABILIZATION = 4
    COMPLETE = 5


@dataclass
class RecoveryPhaseRecord:
    """Per-phase timing, SOC trajectory, and fuel consumption record."""
    phase: RecoveryPhase
    entry_time: Optional[datetime] = None
    exit_time: Optional[datetime] = None
    duration_steps: int = 0
    avg_battery_soc: Dict[str, float] = field(default_factory=dict)
    gen_fuel_consumed_liters: Dict[str, float] = field(default_factory=dict)
    stability_checks_passed: int = 0
    stability_checks_failed: int = 0
    load_ramp_factor: float = 0.0


@dataclass
class StabilityEvent:
    """Individual stability check event for temporal analysis."""
    timestamp: Optional[datetime]
    mg_id: str
    check_type: str   # 'load_pickup', 'soc_drop', 'fuel_critical', 'city_load'
    value: float
    threshold: float
    passed: bool


class RecoveryController:
    """
    4-phase staged reconnection controller.

    Phase sequence:
    1. PREPARATION: Verify grid stability, pre-charge batteries
    2. CRITICAL_RECONNECT: Reconnect hospital, wait for stability
    3. STAGED_RESTORATION: Reconnect remaining MGs in priority order
    4. STABILIZATION: Generator ramp-down, battery recovery, monitoring
    """

    RECONNECTION_SEQUENCE = [
        'hospital', 'university', 'industrial', 'residential'
    ]

    LOAD_RAMP_SCHEDULE = [0.25, 0.50, 0.75, 1.00]
    GEN_RAMPDOWN_RATE = 0.10      # 10% per step
    STABILITY_CHECK_STEPS = 2     # Wait 2 steps (30 min) between reconnections
    STABILIZATION_STEPS = 4       # Monitor for 1 hour
    MIN_BATTERY_FOR_RECONNECT = 60.0  # % SOC before reconnecting

    def __init__(self):
        self.phase = RecoveryPhase.INACTIVE
        self.phase_entry_step = 0
        self.steps_in_phase = 0
        self.reconnected_mgs: List[str] = []
        self.next_reconnect_idx = 0
        self.load_ramp_step = 0
        self.gen_ramp_factor: Dict[str, float] = {}
        # Phase records for per-phase analysis
        self.phase_records: List[RecoveryPhaseRecord] = []
        self._current_record: Optional[RecoveryPhaseRecord] = None
        self._phase_soc_samples: Dict[str, List[float]] = {}
        self._phase_fuel_start: Dict[str, float] = {}

    def start_recovery(self, current_step: int):
        """Initiate recovery sequence when grid restoration is detected."""
        if self.phase == RecoveryPhase.INACTIVE:
            self.phase = RecoveryPhase.PREPARATION
            self.phase_entry_step = current_step
            self.steps_in_phase = 0
            self._start_phase_record(RecoveryPhase.PREPARATION)
            logger.info("RECOVERY: Entering PREPARATION phase")

    def _start_phase_record(self, phase: RecoveryPhase, ts: Optional[datetime] = None):
        """Begin tracking a new recovery phase."""
        if self._current_record is not None:
            self._finalize_phase_record()
        self._current_record = RecoveryPhaseRecord(
            phase=phase, entry_time=ts, duration_steps=0
        )
        self._phase_soc_samples = {}
        self._phase_fuel_start = {}

    def _finalize_phase_record(self, ts: Optional[datetime] = None):
        """Close current phase record and store it."""
        if self._current_record is None:
            return
        self._current_record.exit_time = ts
        self._current_record.duration_steps = self.steps_in_phase
        # Compute average SOC per MG during this phase
        for mg_id, samples in self._phase_soc_samples.items():
            if samples:
                self._current_record.avg_battery_soc[mg_id] = (
                    sum(samples) / len(samples)
                )
        self.phase_records.append(self._current_record)
        self._current_record = None

    def _record_step_snapshot(self, statuses: Dict[str, MicrogridStatus]):
        """Record per-step SOC and fuel for current phase."""
        for mg_id, status in statuses.items():
            self._phase_soc_samples.setdefault(mg_id, []).append(
                status.battery_soc_percent
            )
            if mg_id not in self._phase_fuel_start:
                self._phase_fuel_start[mg_id] = status.fuel_remaining_liters

    def update(
        self,
        mg_statuses: Dict[str, MicrogridStatus],
        mg_infos: Dict,
        current_step: int,
        grid_available: Dict[str, bool],
    ) -> Dict[str, SupervisoryCommand]:
        """
        Execute one recovery step. Returns supervisory commands.

        Args:
            mg_statuses: Current MG statuses.
            mg_infos: MG info registry.
            current_step: Current simulation step.
            grid_available: Dict of mg_id → grid availability.

        Returns:
            Dict of SupervisoryCommand per microgrid.
        """
        commands = {}
        self.steps_in_phase += 1
        timestamp = datetime.now()
        if mg_statuses:
            first_status = next(iter(mg_statuses.values()))
            timestamp = first_status.timestamp

        # Record step snapshot for current phase
        self._record_step_snapshot(mg_statuses)

        if self.phase == RecoveryPhase.PREPARATION:
            commands = self._phase_preparation(mg_statuses, mg_infos, timestamp)

        elif self.phase == RecoveryPhase.CRITICAL_RECONNECT:
            commands = self._phase_critical_reconnect(
                mg_statuses, mg_infos, timestamp
            )

        elif self.phase == RecoveryPhase.STAGED_RESTORATION:
            commands = self._phase_staged_restoration(
                mg_statuses, mg_infos, timestamp
            )

        elif self.phase == RecoveryPhase.STABILIZATION:
            commands = self._phase_stabilization(
                mg_statuses, mg_infos, timestamp
            )

        return commands

    def _phase_preparation(self, statuses, infos, ts) -> Dict:
        """Phase 1: Verify stability, pre-charge batteries."""
        commands = {}

        all_ready = True
        for mg_id, status in statuses.items():
            cmd = SupervisoryCommand(
                microgrid_id=mg_id, timestamp=ts,
                battery_soc_target_percent=80.0,
                reason="Recovery PREPARATION: pre-charging battery"
            )

            # Check readiness
            if status.battery_soc_percent < self.MIN_BATTERY_FOR_RECONNECT:
                cmd.generator_enable = True  # Use gen to charge
                all_ready = False

            # Reduce gen by 25% in preparation
            self.gen_ramp_factor[mg_id] = 0.75
            commands[mg_id] = cmd

        # Advance to next phase after 2 steps or when ready
        if all_ready or self.steps_in_phase >= self.STABILITY_CHECK_STEPS:
            self._start_phase_record(RecoveryPhase.CRITICAL_RECONNECT, ts)
            self.phase = RecoveryPhase.CRITICAL_RECONNECT
            self.steps_in_phase = 0
            logger.info("RECOVERY: Advancing to CRITICAL_RECONNECT")

        return commands

    def _phase_critical_reconnect(self, statuses, infos, ts) -> Dict:
        """Phase 2: Reconnect hospital first, wait for stability."""
        commands = {}
        hospital_id = self.RECONNECTION_SEQUENCE[0]

        for mg_id, status in statuses.items():
            cmd = SupervisoryCommand(microgrid_id=mg_id, timestamp=ts)

            if mg_id == hospital_id:
                cmd.target_shed_percent = 0.0
                cmd.battery_soc_target_percent = 85.0
                cmd.reason = "Recovery: Hospital reconnected (P1)"
                if mg_id not in self.reconnected_mgs:
                    self.reconnected_mgs.append(mg_id)
            else:
                cmd.battery_soc_target_percent = 80.0
                cmd.reason = "Recovery: Awaiting reconnection"

            commands[mg_id] = cmd

        # Wait for stability then advance
        if self.steps_in_phase >= self.STABILITY_CHECK_STEPS:
            self._start_phase_record(RecoveryPhase.STAGED_RESTORATION, ts)
            self.phase = RecoveryPhase.STAGED_RESTORATION
            self.steps_in_phase = 0
            self.next_reconnect_idx = 1
            self.load_ramp_step = 0
            logger.info("RECOVERY: Advancing to STAGED_RESTORATION")

        return commands

    def _phase_staged_restoration(self, statuses, infos, ts) -> Dict:
        """Phase 3: Reconnect remaining MGs in priority order."""
        commands = {}

        # Determine which MG to reconnect this step
        reconnect_mg = None
        if (self.next_reconnect_idx < len(self.RECONNECTION_SEQUENCE)
                and self.steps_in_phase % (self.STABILITY_CHECK_STEPS + 1) == 0):
            reconnect_mg = self.RECONNECTION_SEQUENCE[self.next_reconnect_idx]
            if reconnect_mg not in self.reconnected_mgs:
                self.reconnected_mgs.append(reconnect_mg)
                logger.info(f"RECOVERY: Reconnecting {reconnect_mg}")
            self.next_reconnect_idx += 1

        # Compute load ramp for reconnected MGs
        ramp_idx = min(self.load_ramp_step, len(self.LOAD_RAMP_SCHEDULE) - 1)
        ramp_factor = self.LOAD_RAMP_SCHEDULE[ramp_idx]

        for mg_id, status in statuses.items():
            cmd = SupervisoryCommand(microgrid_id=mg_id, timestamp=ts)

            if mg_id in self.reconnected_mgs:
                # Gradual load pickup
                shed_pct = (1.0 - ramp_factor) * 100.0
                cmd.target_shed_percent = max(0.0, shed_pct)
                cmd.battery_soc_target_percent = 85.0
                cmd.reason = (
                    f"Recovery: {mg_id} at {ramp_factor:.0%} load "
                    f"(ramp step {ramp_idx + 1})"
                )
            else:
                cmd.battery_soc_target_percent = 80.0
                cmd.reason = "Recovery: Awaiting reconnection"

            commands[mg_id] = cmd

        self.load_ramp_step += 1

        # Advance when all MGs are reconnected and at full load
        if (self.next_reconnect_idx >= len(self.RECONNECTION_SEQUENCE)
                and ramp_factor >= 1.0):
            self._start_phase_record(RecoveryPhase.STABILIZATION, ts)
            self.phase = RecoveryPhase.STABILIZATION
            self.steps_in_phase = 0
            logger.info("RECOVERY: Advancing to STABILIZATION")

        return commands

    def _phase_stabilization(self, statuses, infos, ts) -> Dict:
        """Phase 4: Generator ramp-down, battery recovery, monitoring."""
        commands = {}

        for mg_id, status in statuses.items():
            cmd = SupervisoryCommand(microgrid_id=mg_id, timestamp=ts)

            # Generator ramp-down
            current_factor = self.gen_ramp_factor.get(mg_id, 1.0)
            new_factor = max(0.0, current_factor - self.GEN_RAMPDOWN_RATE)
            self.gen_ramp_factor[mg_id] = new_factor

            if new_factor <= 0.0:
                cmd.generator_enable = False
                cmd.reason = "Recovery: Generator off, stabilized"
            else:
                cmd.generator_enable = True
                cmd.reason = (
                    f"Recovery: Gen ramp-down {new_factor:.0%} "
                    f"(step {self.steps_in_phase})"
                )

            # Battery: float charge to 90%
            cmd.battery_soc_target_percent = 90.0
            cmd.target_shed_percent = 0.0

            commands[mg_id] = cmd

        # Complete after monitoring period
        if self.steps_in_phase >= self.STABILIZATION_STEPS:
            self._finalize_phase_record(ts)
            self.phase = RecoveryPhase.COMPLETE
            logger.info("RECOVERY: COMPLETE — all systems normal")

        return commands

    @property
    def is_active(self) -> bool:
        return self.phase not in [RecoveryPhase.INACTIVE, RecoveryPhase.COMPLETE]

    @property
    def is_complete(self) -> bool:
        return self.phase == RecoveryPhase.COMPLETE


